function solve(input: string) {
    const tiles = input.split('\n\n').map(s => {
        const [head, body] = s.split(/:\n/)
        const [, id] = head.split(' ').map(n => parseInt(n))
        const tile = body.split('\n').map(s => s.split(''))
        return {id, data: tile}
    })

    return tiles.filter((tile, i) => {
        return tiles.filter((otherTile, otherI) => {
            return otherI !== i && match(tile.data, otherTile.data)
        }).length === 2
    })
        .map(x => x.id)
        .reduce((acc, x) => acc * x, 1)
}

function match(tile: string[][], otherTile: string[][]) {
    return variations(tile).filter(a =>
        variations(otherTile).filter(b => JSON.stringify(a[0]) === JSON.stringify(b[0])).length > 0
    ).length > 0
}

function variations(tile: string[][]) {
    const result = []
    const flipped = flip(tile)
    const flipped2 = flip2(tile)
    const seeds = [tile, flipped, flipped2]

    seeds.forEach(variation => {
        result.push(variation)
        result.push(variation = rotate(variation))
        result.push(variation = rotate(variation))
        result.push(rotate(variation))
    })
    return result
}

function flip(tile: string[][]) {
    return tile.map(row => row.map(a => a).reverse())
}

function flip2(tile: string[][]) {
    return tile.map(a => a).reverse()
}

function rotate(tile: string[][]) {
    const result = []
    for (let i = 0; i < tile[0].length; ++i) {
        const newRow = []
        for (const row of tile) {
            newRow.push(row[i])
        }
        result.push(newRow.reverse())
    }
    return result
}

